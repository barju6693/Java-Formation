package Calculadora;

public class Calculadora {

	public Calculadora(){

    }

    public int mult(int x, int y){
        return x*y;
    }
    public int sum(int x, int y){
        return x+y;
    }
    public int rest(int x, int y){
        return x-y;
    }
    public float div(int x, int y){
        return (float)x/y;
    }
}
